package java09212020;

public class OverloadTest {
	public static void main(String[] args) {
		// Method Overloading examples
		System.out.print("Void :");	        System.out.println();
		System.out.print("String Type :");  System.out.println("apple"); 
		System.out.print("Char Type :");    System.out.println('A');
		System.out.print("Integer Type :"); System.out.println(25);
		System.out.print("Double Type : "); System.out.println(25.5);
		
	}
	
	

}
